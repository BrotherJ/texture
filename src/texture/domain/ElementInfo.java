package texture.domain;

/**
 * 元素清单
 * @author jiong.sun
 *
 */
public class ElementInfo {

	
	private String id;
	/**元素名称**/
	private String name;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
