package texture.domain;

/**
 * 硬度 hrc 与hb的对照表
 * @author jiong.sun
 *
 */
public class Hard {

	private String id;
	/**hrc硬度**/
	private String hrc;
	/**hb硬度**/
	private String hb;
	/**HV硬度**/
	private String hv;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getHrc() {
		return hrc;
	}
	public void setHrc(String hrc) {
		this.hrc = hrc;
	}
	public String getHb() {
		return hb;
	}
	public void setHb(String hb) {
		this.hb = hb;
	}
	public String getHv() {
		return hv;
	}
	public void setHv(String hv) {
		this.hv = hv;
	}
	
	
	
}
